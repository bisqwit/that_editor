$n=0;
foreach($conv as $a => $index)
{
  $c = $index & 0x1F;
  switch($index & 0xE0)
  {
    case 0x40:
      if($a == 123) { $c = 0x55; break; }
      if($a == 125) { $c = 0x4B; break; }
      if($a == 124) { $c = 0x42; break; }
      $c |= 0x100;
      break;
    case 0xA0:
      if($a >= 0xC0) { $c |= 0x40; break; }
      $c |= 0x60;
      break;
    case 0xC0:
      if($a >= 0xC0) { $c |= 0x40; break; }
      if($a == 92) { $c = 0x4D; break; }
      $c |= 0x00;
      break;
    default: $c = $index; break;
  }
  $c *= 8;
  
  if($n == ord('_'))
    $font[$c+7] = chr(0xFF);

  if(($n==0 || $n >= 0x21) && $n < 0xE0)
  {
    for($a=0; $a<8; ++$a)
      printf('0x%02X,%s', ord($font[$c+$a]), $a==7?"\n":"");
  }
  #print substr($font, $c, 8);
  ++$n;
}
